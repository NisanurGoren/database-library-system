
-- 1. Kullanıcı Tipleri
CREATE TABLE KULLANICI_TIPLERI (
    TIP_ID NUMBER PRIMARY KEY,
    TIP_ADI VARCHAR2(20) NOT NULL
);

-- 2. Kullanıcılar
CREATE TABLE KULLANICILAR (
    KULLANICI_ID CHAR(4) PRIMARY KEY,
    TIP_ID NUMBER NOT NULL REFERENCES KULLANICI_TIPLERI(TIP_ID),
    AD VARCHAR2(50) NOT NULL,
    SOYAD VARCHAR2(50) NOT NULL
);

-- 3. Konumlar
CREATE TABLE KONUMLAR (
    KONUM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    KONUM_ADI VARCHAR2(50) NOT NULL
);

-- 4. Masalar
CREATE TABLE MASALAR (
    MASA_ID NUMBER PRIMARY KEY,
    KONUM_ID NUMBER NOT NULL REFERENCES KONUMLAR(KONUM_ID),
    DURUM CHAR(1) DEFAULT 'N' CHECK (DURUM IN ('N', 'Y')),
    AKTIF_KULLANICI CHAR(4) REFERENCES KULLANICILAR(KULLANICI_ID),
    BASLANGIC_SAAT DATE,
    BITIS_SAAT DATE
);

-- 5. Fiş Kayıtları
CREATE TABLE FIS_KAYITLARI (
    FIS_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    KULLANICI_ID CHAR(4) NOT NULL REFERENCES KULLANICILAR(KULLANICI_ID),
    MASA_ID NUMBER NOT NULL REFERENCES MASALAR(MASA_ID),
    BASLANGIC_SAAT DATE DEFAULT SYSDATE,
    BITIS_SAAT DATE,
    AKTIF_MI CHAR(1) DEFAULT 'Y' CHECK (AKTIF_MI IN ('Y', 'N'))
);

-- 6. Trigger: FIS_KAYITLARI Ekleme ile MASALAR Güncelle
CREATE OR REPLACE TRIGGER trg_fis_kayit_ekle
AFTER INSERT ON FIS_KAYITLARI
FOR EACH ROW
BEGIN
    UPDATE MASALAR
    SET DURUM = 'Y',
        AKTIF_KULLANICI = :NEW.KULLANICI_ID,
        BASLANGIC_SAAT = :NEW.BASLANGIC_SAAT,
        BITIS_SAAT = :NEW.BITIS_SAAT
    WHERE MASA_ID = :NEW.MASA_ID;
END;
/

-- 7. Trigger: 90 Dakika Sonrası Masa Boşalt (manuel test için kullanılacak)
CREATE OR REPLACE TRIGGER trg_masa_bosalt
BEFORE UPDATE ON MASALAR
FOR EACH ROW
WHEN (NEW.BITIS_SAAT <= SYSDATE)
BEGIN
    :NEW.DURUM := 'N';
    :NEW.AKTIF_KULLANICI := NULL;
    :NEW.BASLANGIC_SAAT := NULL;
    :NEW.BITIS_SAAT := NULL;

    UPDATE FIS_KAYITLARI
    SET AKTIF_MI = 'N'
    WHERE MASA_ID = :OLD.MASA_ID AND AKTIF_MI = 'Y';
END;
/

