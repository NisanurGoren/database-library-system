DECLARE
    v_start DATE := SYSDATE;
BEGIN
    FOR i IN 1..10 LOOP
        INSERT INTO FIS_KAYITLARI (KULLANICI_ID, MASA_ID, BASLANGIC_SAAT, BITIS_SAAT)
        VALUES (
            TO_CHAR(1000 + i),
            i,
            v_start + (i / 1440),
            v_start + (i / 1440) + (90 / 1440)
        );
    END LOOP;
END;
/

-- Masaya oturtma prosedürü
CREATE OR REPLACE PROCEDURE OTURT_KULLANICI (
    p_kullanici_id IN CHAR,
    p_masa_id IN NUMBER
) AS
    v_now DATE := SYSDATE;
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM FIS_KAYITLARI
    WHERE KULLANICI_ID = p_kullanici_id AND AKTIF_MI = 'Y';

    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Kullanıcının zaten aktif bir oturumu var.');
    END IF;

    INSERT INTO FIS_KAYITLARI (
        KULLANICI_ID, MASA_ID, BASLANGIC_SAAT, BITIS_SAAT
    ) VALUES (
        p_kullanici_id,
        p_masa_id,
        v_now,
        v_now + (90 / 1440)
    );
END;
